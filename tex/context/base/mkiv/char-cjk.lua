local cjk_ideograph_metatable = {
    __index = {
        category    = "lo",
        cjkwd       = "w",
        description = "<CJK Ideograph>",
        direction   = "l",
        linebreak   = "id",
    }
}

local f_variant_name = formatters["CJK COMPATIBILITY IDEOGRAPH-%X"]

local variants = setmetatableindex(function(t,k)
    local kv = knownvariants[k]
    if kv then
        for k, v in next, kv do
            kv[k] = f_variant_name(v)
        end
    end
    knownvariants[k] = nil
    return kv
end)

local cjk_ideograph_extender = function(k)
    local t = {
     -- shcode      = shcode,
        unicodeslot = k,
        variants    = variants[k],
    }
    setmetatable(t,cjk_ideograph_metatable)
    return t
end

local cjk_ideograph_range = {
    name     = "cjk ideograph",
    first    = 0x4E00,
    last     = 0x9FBB,
    extender = cjk_ideograph_extender,
}

